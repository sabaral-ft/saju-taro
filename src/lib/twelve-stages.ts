/**
 * 사주 네 기둥의 12운성 모두 계산
 */
export function calculateAllTwelveStages(
  ilgan: string,
  yearJiji: string,
  monthJiji: string,
  dayJiji: string,
  hourJiji: string,
  useDongsaeng: boolean = false
): {
  year: TwelveStage;
  month: TwelveStage;
  day: TwelveStage;
  hour: TwelveStage;
  dominant: TwelveStage;
  energyTotal: number;
} {
  const year = calculateTwelveStage(ilgan, yearJiji, useDongsaeng);
  const month = calculateTwelveStage(ilgan, monthJiji, useDongsaeng);
  const day = calculateTwelveStage(ilgan, dayJiji, useDongsaeng);
  const hour = calculateTwelveStage(ilgan, hourJiji, useDongsaeng);

  // 가장 에너지가 높은 운성
  const stages = [
    { stage: year, data: TWELVE_STAGE_DATA[year] },
    { stage: month, data: TWELVE_STAGE_DATA[month] },
    { stage: day, data: TWELVE_STAGE_DATA[day] },
    { stage: hour, data: TWELVE_STAGE_DATA[hour] },
  ];

  const dominant = stages.sort((a, b) => b.data.energy - a.data.energy)[0].stage;
  const energyTotal = stages.reduce((sum, s) => sum + s.data.energy, 0);

  return { year, month, day, hour, dominant, energyTotal };
}
