/**
 * 12운성 (十二運星) 시스템
 * 천간이 지지를 만났을 때의 에너지 상태를 12단계로 나타냄
 * 사람의 인생 주기를 상징: 태아→탄생→성장→전성기→쇠퇴→죽음→재생
 */

export type TwelveStage =
  | '장생' | '목욕' | '관대' | '건록' | '제왕'
  | '쇠' | '병' | '사' | '묘' | '절' | '태' | '양';

// 12운성 순서
export const TWELVE_STAGES: TwelveStage[] = [
  '장생', '목욕', '관대', '건록', '제왕',
  '쇠', '병', '사', '묘', '절', '태', '양'
];

/**
 * 천간별 장생지 (12운성의 시작점이 되는 지지)
 * 양간은 순행, 음간은 역행
 */
const JANGSEONG_JI: Record<string, number> = {
  '갑': 11, // 해(亥)
  '을': 6,  // 오(午)
  '병': 2,  // 인(寅)
  '정': 9,  // 유(酉)
  '무': 2,  // 인(寅)
  '기': 9,  // 유(酉)
  '경': 5,  // 사(巳)
  '신': 0,  // 자(子)
  '임': 8,  // 신(申)
  '계': 3,  // 묘(卯)
};

// 양간 여부
const IS_YANG: Record<string, boolean> = {
  '갑': true, '을': false,
  '병': true, '정': false,
  '무': true, '기': false,
  '경': true, '신': false,
  '임': true, '계': false,
};

const JIJI_LIST = ['자', '축', '인', '묘', '진', '사', '오', '미', '신', '유', '술', '해'];

/**
 * 12운성 계산
 * @param cheongan 천간 (예: '갑')
 * @param jiji 지지 (예: '자')
 * @param useDongsaeng 동생동사(同生同死) 모드: true이면 음간도 양간과 같은 방향(순행)으로 계산
 *   - false(기본값) = 음생양사(陰生陽死): 양간 순행, 음간 역행 (전통 방식)
 *   - true = 동생동사(同生同死): 음양 구분 없이 모두 순행
 * @returns 12운성 이름
 */
export function calculateTwelveStage(cheongan: string, jiji: string, useDongsaeng: boolean = false): TwelveStage {
  const jijiIdx = JIJI_LIST.indexOf(jiji);
  const startIdx = JANGSEONG_JI[cheongan];
  const isYang = IS_YANG[cheongan];

  let offset: number;
  if (isYang || useDongsaeng) {
    // 양간이거나 동생동사 모드면 순행
    offset = (jijiIdx - startIdx + 12) % 12;
  } else {
    // 음간이고 음생양사 모드면 역행
    offset = (startIdx - jijiIdx + 12) % 12;
  }

  return TWELVE_STAGES[offset];
}

