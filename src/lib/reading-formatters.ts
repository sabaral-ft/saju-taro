export function sipseongLabel(sip: string, showDesc = false): string {
  const info = SIPSEONG_EASY[sip];
  if (!info) return sip;
  if (showDesc) return `${info.emoji} ${sip}(${info.name}) — ${info.desc}`;
  return `${info.emoji} ${sip}(${info.name})`;
}

export function adaptLoveTextForMarried(text: string): string {
    return text
      // 새 인연 → 배우자 관계 재발견
      .replace(/새로운 인연이 기다립니다/g, '배우자와의 관계가 새롭게 피어납니다')
      .replace(/싱글이라면 매력적인 만남이 예상됩니다/g, '부부 사이에 설렘이 다시 찾아올 수 있습니다')
      .replace(/운명적인 만남이 있을 수 있습니다/g, '배우자와 운명적 유대가 더 깊어질 수 있습니다')
      .replace(/운명적인 만남의 시작입니다/g, '배우자와의 인연이 다시 깊어지는 시기입니다')
      .replace(/설레는 새로운 인연의 시작입니다/g, '배우자와의 관계에 새로운 설렘이 찾아옵니다')
      .replace(/설레는 메시지나 만남이 기다립니다/g, '배우자로부터 기분 좋은 소식이나 이벤트가 있을 수 있습니다')
      // 첫사랑/재회 → 부부 추억
      .replace(/첫사랑이나 옛 인연과 재회할 수 있습니다/g, '배우자와 처음 만났던 설렘을 다시 느낄 수 있는 시기예요. 추억의 장소를 함께 방문해보세요')
      .replace(/과거 연인에 대한 미련을 놓으세요/g, '과거에 대한 아쉬움보다 현재 배우자와의 시간에 집중하세요')
      // 고백/프러포즈 → 감사 표현
      .replace(/고백이나 프러포즈에도 좋은 운입니다/g, '부부간 감사 표현이나 깜짝 이벤트에 좋은 운입니다')
      .replace(/적극적인 구애가 성공합니다/g, '배우자에게 적극적인 애정 표현이 효과적입니다')
      .replace(/순수한 고백이나 러브레터를 받을 수 있습니다/g, '배우자로부터 따뜻한 감사의 말이나 깜짝 선물을 받을 수 있어요')
      .replace(/로맨틱한 프러포즈나 고백이 있습니다/g, '배우자와 로맨틱한 기념일이나 특별한 이벤트를 즐길 수 있어요')
      // 이별/떠남 → 관계 위기 극복
      .replace(/관계를 떠나 새로운 길을 찾아야 할 때입니다/g, '부부 관계에서 변화가 필요한 시점이에요. 함께 새로운 방향을 모색해보세요')
      .replace(/떠날 용기가 나지 않지만 변화가 필요합니다/g, '관계의 패턴을 바꿔야 하지만 쉽지 않은 시기예요. 부부 상담을 고려해보세요')
      .replace(/이별도 새로운 시작/g, '위기도 관계를 재정립하는 기회')
      // 새 사랑/열림 → 부부 재점화
      .replace(/새로운 사랑에 마음이 열리고 있습니다/g, '배우자에 대한 마음이 다시 열리고 있습니다')
      .replace(/마음을 닫고 있어 인연을 놓칩니다/g, '마음을 닫고 있어 배우자와 소통이 막히고 있습니다')
      // 미성숙/가벼운 접근 → 부부 맥락
      .replace(/가벼운 마음의 접근이 문제가 됩니다/g, '배우자를 가볍게 대하면 신뢰가 흔들릴 수 있어요')
      .replace(/감정적으로 미성숙한 만남을 주의하세요/g, '부부간 감정적 소통이 미숙하면 오해가 쌓일 수 있어요')
      .replace(/말뿐인 약속을 주의하세요/g, '배우자에게 한 약속은 꼭 지키세요')
      // 기존 패턴
      .replace(/아무나 만나지 마세요/g, '외부 유혹에 흔들리지 마세요')
      .replace(/연인과의 관계가 깊어지고/g, '부부 사이의 유대가 깊어지고')
      .replace(/연인에게 든든한 존재/g, '배우자에게 든든한 존재')
      .replace(/삼각관계, 바람, 이별의 위험/g, '외도나 신뢰 균열의 위험')
      .replace(/인연의 순환 속에서 좋은 사람이 나타납니다/g, '부부 관계에 긍정적 전환이 찾아옵니다')
      .replace(/사랑이 무르익는 시기/g, '부부 사랑이 무르익는 시기')
      // 포괄적 패턴 (위 정확한 매칭 후 남은 것 처리)
      .replace(/새로운 인연/g, '배우자와의 새로운 시작')
      .replace(/좋은 인연을 만날/g, '배우자와 더 좋은 관계를 만들');
}

export function easySipseongText(text: string): string {
      let result = text;
      for (const [term, info] of Object.entries(SIPSEONG_EASY)) {
        // "식신이" → "식신(재능·아이디어)이" 처럼 조사가 붙은 경우도 처리
        const regex = new RegExp(`(?<![가-힣])${term}(?![\\(])`, 'g');
        result = result.replace(regex, `${term}(${info.name})`);
      }
      return result;
}
